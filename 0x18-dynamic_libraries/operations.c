#include <stdlib.h>
#include <stdio.h>

/**
 * add - function to add
 * @a: first int
 * @b: second int
 * Return: returns result
 * */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function to add
 * @a: first int
 * @b: second int
 * Return: returns result
 * */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function to add
 * @a: first int
 * @b: second int
 * Return: returns result
 * */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function to add
 * @a: first int
 * @b: second int
 * Return: returns result
 * */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function to add
 * @a: first int
 * @b: second int
 * Return: returns result
 * */
int mod(int a, int b)
{
	return (a % b);
}
